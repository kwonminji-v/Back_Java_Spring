package model;

public class Study_0612_Ex_2_1_KeyValue <K, V> {
	
	    private K key;
	    private V value;

	    public void setKey(K key) {
	        this.key = key;
	    }

	    public void setValue(V value) {
	        this.value = value;
	    }

	    public K getKey() {
	        return key;
	    }

	    public V getValue() {
	        return value;
	    }
	}



